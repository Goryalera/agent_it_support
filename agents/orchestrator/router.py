"""Маршрутизация запроса: какой специалист должен ответить.

Три маршрута:
- "rag"     — вопрос на знание (как настроить, где взять, что делать).
- "analyst" — вопрос про статистику/отчёт (сколько, динамика, среднее).
- "action"  — запрос-действие (создай заявку, оформи тикет, запроси доступ).

Решение принимает LLM (классификация интента). Запасной вариант — простое
правило по ключевым словам: оно срабатывает, если LLM недоступен, ответил
непонятно или работает в mock-режиме. Так маршрутизация остаётся
детерминированной и предсказуемой даже без реального LLM.
"""

from __future__ import annotations

from shared.llm import ask_llm

ROUTE_RAG = "rag"
ROUTE_ANALYST = "analyst"
ROUTE_ACTION = "action"
_VALID = {ROUTE_RAG, ROUTE_ANALYST, ROUTE_ACTION}

# Ключевые слова запроса-действия (создать/оформить заявку или тикет).
# Проверяются раньше аналитики: «создай» важнее, чем случайные слова статистики.
_ACTION_KEYWORDS = (
    "создай",
    "создать",
    "оформи",
    "оформить",
    "заведи",
    "завести",
    "подай заявк",
    "подать заявк",
    "запроси",
    "запросить доступ",
    "открой тикет",
    "открыть тикет",
    "сделай заявк",
    "зарегистрируй",
)

# Ключевые слова, характерные для аналитических вопросов (статистика/отчёты).
_ANALYST_KEYWORDS = (
    "сколько",
    "статистик",
    "отчёт",
    "отчет",
    "среднее",
    "средн",
    "динамик",
    "количество",
    "сколько раз",
    "топ ",
    "топ-",
    "за май",
    "за месяц",
    "за неделю",
    "за год",
    "по категори",
    "в разрезе",
    "процент",
    "доля",
    "тренд",
    "график",
    "распределение",
    "сгруппир",
)

_CLASSIFY_SYSTEM = (
    "Ты — маршрутизатор запросов ИТ-поддержки. Определи тип запроса и ответь "
    "РОВНО одним словом без пояснений:\n"
    "- 'rag' — вопрос на знание: как что-то сделать, настроить, где получить "
    "доступ, инструкция (например: «как настроить VPN»).\n"
    "- 'analyst' — вопрос про статистику или отчёт по тикетам: сколько, динамика, "
    "среднее, в разрезе категорий (например: «сколько инцидентов по доступам за "
    "май»).\n"
    "- 'action' — запрос-действие: создать заявку или тикет, оформить запрос на "
    "доступ/оборудование (например: «создай заявку на доступ к боевой БД»).\n"
    "Ответ — только одно слово: rag, analyst или action."
)


def _keyword_route(question: str) -> str:
    """Запасное правило: действие → аналитика → иначе RAG (порядок важен)."""
    text = question.lower()
    if any(kw in text for kw in _ACTION_KEYWORDS):
        return ROUTE_ACTION
    if any(kw in text for kw in _ANALYST_KEYWORDS):
        return ROUTE_ANALYST
    return ROUTE_RAG


def _parse_llm_route(raw: str) -> str | None:
    """Достать корректный маршрут из ответа LLM, иначе None."""
    text = (raw or "").strip().lower()
    # Берём первое встретившееся валидное слово (модель иногда добавляет лишнее).
    for token in text.replace("'", " ").replace('"', " ").split():
        if token in _VALID:
            return token
    return None


def classify_intent(question: str) -> str:
    """Выбрать маршрут для вопроса: 'rag' или 'analyst'.

    Сначала пробуем LLM-классификацию; если ответ невалиден или вызов упал —
    откатываемся на ключевые слова.
    """
    question = (question or "").strip()
    if not question:
        return ROUTE_RAG

    try:
        raw = ask_llm(question, system=_CLASSIFY_SYSTEM)
        route = _parse_llm_route(raw)
        if route is not None:
            return route
    except Exception:  # noqa: BLE001 — классификация не должна ронять запрос
        pass

    # LLM не дал валидного ответа (или mock-режим) — решаем по ключевым словам.
    return _keyword_route(question)
